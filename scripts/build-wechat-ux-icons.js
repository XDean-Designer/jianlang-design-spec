/**
 * WeChat UX Kit icons from Figma Community (Le Wagon).
 * Manifest: scripts/wechat-ux-asset-manifest.json (asset URLs from Figma MCP export).
 * Figma: https://www.figma.com/design/UqA4U4cmf68pkXynOIhPHU/
 */
const fs = require('fs');
const path = require('path');
const https = require('https');

const MANIFEST = path.join(__dirname, 'wechat-ux-asset-manifest.json');
const OUT_LIGHT = path.join(__dirname, '..', 'icons', 'wechat');
const OUT_FILLED = path.join(__dirname, '..', 'icons', 'wechat-filled');
const MAP_LIGHT_JS = path.join(__dirname, '..', 'icons', 'wechat-map.js');
const MAP_FILLED_JS = path.join(__dirname, '..', 'icons', 'wechat-filled-map.js');

function fetchUrl(url) {
  return new Promise((resolve, reject) => {
    https.get(url, res => {
      if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
        return fetchUrl(res.headers.location).then(resolve, reject);
      }
      const chunks = [];
      res.on('data', c => chunks.push(c));
      res.on('end', () => {
        if (res.statusCode !== 200) {
          reject(new Error(`${res.statusCode} ${url}`));
          return;
        }
        resolve(Buffer.concat(chunks).toString('utf8'));
      });
    }).on('error', reject);
  });
}

function normalizeWechatSvg(content, filled) {
  if (!/^\s*<svg/i.test(content)) return content;
  let s = content.replace(/<svg([^>]*)>/i, (_, attrs) => {
    let a = attrs.replace(/\s*fill="[^"]*"/gi, '');
    if (!filled) a = a.includes('fill=') ? a : `${a} fill="none"`;
    return `<svg${a}>`;
  });
  if (filled) {
    s = s.replace(/\bfill="[^"]*"/gi, 'fill="currentColor"');
    s = s.replace(/\bfill-opacity="[^"]*"/gi, '');
  } else {
    s = s.replace(/<(\w+)([^>]*?)(\s*\/?)>/gi, (match, tag, attrs, selfClose) => {
      if (!/^(path|circle|rect|ellipse|line|polyline|polygon)$/i.test(tag)) return match;
      let a = attrs.replace(/\s*fill="[^"]*"/gi, '');
      const hasStroke = /\bstroke\s*=\s*"(?!none")/i.test(attrs);
      if (hasStroke) {
        a = a.replace(/\s*stroke="[^"]*"/gi, '');
        a += ' fill="none" stroke="currentColor"';
        if (!/stroke-width=/i.test(attrs)) a += ' stroke-width="1.5"';
      } else {
        a += ' fill="currentColor"';
      }
      return `<${tag}${a}${selfClose || ''}>`;
    });
  }
  if (!/viewBox=/i.test(s)) {
    s = s.replace('<svg', '<svg viewBox="0 0 24 24"');
  }
  return s;
}

async function main() {
  const { icons } = JSON.parse(fs.readFileSync(MANIFEST, 'utf8'));
  fs.mkdirSync(OUT_LIGHT, { recursive: true });
  fs.mkdirSync(OUT_FILLED, { recursive: true });

  const mapLight = {};
  const mapFilled = {};

  for (const item of icons) {
    const filled = item.variant === 'filled';
    const dir = filled ? OUT_FILLED : OUT_LIGHT;
    const file = `${item.slug}.svg`;
    const outPath = path.join(dir, file);
    process.stdout.write(`fetch ${item.slug} (${item.variant})… `);
    try {
      const raw = await fetchUrl(item.url);
      const svg = normalizeWechatSvg(raw, filled);
      fs.writeFileSync(outPath, svg, 'utf8');
      if (filled) mapFilled[item.slug] = file;
      else mapLight[item.slug] = file;
      console.log('ok');
    } catch (e) {
      console.log('FAIL', e.message);
    }
  }

  const writeMap = (obj, p, constName) => {
    fs.writeFileSync(
      p,
      `/** Auto-generated by scripts/build-wechat-ux-icons.js */\nconst ${constName} = ${JSON.stringify(obj, null, 2)};\n`,
      'utf8'
    );
  };
  writeMap(mapLight, MAP_LIGHT_JS, 'WECHAT_ICON_MAP');
  writeMap(mapFilled, MAP_FILLED_JS, 'WECHAT_FILLED_ICON_MAP');
  console.log(`light: ${Object.keys(mapLight).length} · filled: ${Object.keys(mapFilled).length}`);
}

main().catch(e => {
  console.error(e);
  process.exit(1);
});
